

# string repeater 
# this function take two parameters text and number and then based on the number will repeat the string

def repeat_string(text, number) :  # passing text and number we need to repeat the text 

    return text * number  # multiply the text with the number 

string_multiply_result = repeat_string('Hello\t', 4)  # call the function and pass the arguments values.
print(string_multiply_result)

